

public class DefaulterData {
	
	private String xdefault;
	private String typ;
	private double varde;
	
	public DefaulterData(String xdefault, String typ, double varde) {
		this.xdefault = xdefault;
		this.typ = typ;
		this.varde = varde;
	}

	public String getXdefaulter() {
		return xdefault;
	}

	public String getTyp() {
		return typ;
	}

	public double getVarde() {
		return varde;
	}

}
